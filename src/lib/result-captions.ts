export type CaptionBand = "high" | "balanced" | "low";

/** 高め≥60／中間41〜59／低め≤40 */
export function captionBand(score: number): CaptionBand {
  if (score >= 60) return "high";
  if (score <= 40) return "low";
  return "balanced";
}

